// if statement
if (value === 1) {
  console.log(statement1)
}
// if else statement
if (value === 1) {
  console.log(statement1)
} else {
  console.log(statement2)
}

// ternary operator
(value === 1) ? console.log(statement1) : console.log(statement2)

// Nested if statements
if(value === 1) {
  console.log(statement1)
} else if (value === 2) {
  console.log(statement2)
} else if (value === 3) {
  console.log(statement3)
} else {
  console.log('default')
}

// Switch
let value = 1
switch (value) {
  case 1:
    console.log(statement1)
    break;
  case 2:
    console.log(statement2)
    break;
  case 3:
    console.log(statement3)
    break;
  default:
    console.log('default')
    break;
}

